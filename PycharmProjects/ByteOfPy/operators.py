# * 给出两个数的乘积，或返回字符串重复指定次数后的结果。
print('la' * 3)

# ** （乘方）
print(3 ** 3)

# // （整除）
print(13 // 3)
print(-13 // 3)

# % （取模）
print(13 % 3)
print(12.5 % 4)
print(-13 % 3)

# << （左移）将数字的位向左移动指定的位数。（每个数字在内存中以二进制数表示，即 0 和1）
print(2 << 2)

# >> （右移）
print(11 >> 1)

# & （按位与）
print(5 & 3)

# | （按位或）
print(5 | 3)

# ^ （按位异或）
print(5 ^ 3)

# ~ （按位取反）x 的按位取反结果为 -(x+1)。
print(~5)

# < （小于）所有的比较运算符返回的结果均为 True 或 False 。
print(3 < 5)

# not （布尔“非”）
print(not 3 < 5)
print(not True)

# Shortcut for math operation and assignment
a = 2
a = a * 3
print(a)
a *= 3
print(a)

